package semana01;

public class Operadores {

	public static void main(String[] args) {
		int a = 5;
		int b = 2;

		// Bitwise operators:
		// & (bitwise and), | (bitwise or), ^ (bitwise xor), ~ (bitwise not)
		// << (left shift), >> (right shift), >>> (zero fill right shift)
		System.out.println("a & b = " + (a & b));
		System.out.println("a | b = " + (a | b));

	}

}
